function show()
{
    var htmlbox = document.getElementById("htmlbox");
    
    htmlbox.style.display = "flex";
    htmlbox.style.transition = "1s all linear";
    htmlbox.style.transform = "sacle(0.5,0.5)";
    htmlbox.style.animation = "display 2s linear";    
}
function hide()
{
    var htmlbox = document.getElementById("htmlbox");
    htmlbox.style.display = "none";
}
